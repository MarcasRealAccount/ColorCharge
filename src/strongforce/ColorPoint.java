package strongforce;

public class ColorPoint {
	public float x, y;
	public float r, g, b;
	public float mass;

	public ColorPoint(float x, float y, float r, float g, float b, float mass) {
		this.x    = x;
		this.y    = y;
		this.r    = r;
		this.g    = g;
		this.b    = b;
		this.mass = mass;
	}
}
